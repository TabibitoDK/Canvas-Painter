export class Vector2 {
    constructor(x, y) {
        this.x = x;
        this.y = y;
    }
    getx () {
        return this.x;
    }
    gety () {
        return this.y;
    }
    setx (x) {
        this.x = x;
    }
    sety (y) {
        this.y = y;
    }
    set(x, y) {
        this.x = x;
        this.y = y;
    }
}